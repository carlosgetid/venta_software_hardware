package com.empresa.proyecto.bean;

public class DetCaracteristica {

	private int codDetCate;
	private String procesador;
	private String sist_opera;
	private String memoria_ram;
	private String almacenamiento;
	private String color;
	private String peso;
	private String puerto_usb;
	private String resolucion;
	
	
	public int getCodDetCate() {
		return codDetCate;
	}
	public void setCodDetCate(int codDetCate) {
		this.codDetCate = codDetCate;
	}
	public String getProcesador() {
		return procesador;
	}
	public void setProcesador(String procesador) {
		this.procesador = procesador;
	}
	public String getSist_opera() {
		return sist_opera;
	}
	public void setSist_opera(String sist_opera) {
		this.sist_opera = sist_opera;
	}
	public String getMemoria_ram() {
		return memoria_ram;
	}
	public void setMemoria_ram(String memoria_ram) {
		this.memoria_ram = memoria_ram;
	}
	public String getAlmacenamiento() {
		return almacenamiento;
	}
	public void setAlmacenamiento(String almacenamiento) {
		this.almacenamiento = almacenamiento;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getPeso() {
		return peso;
	}
	public void setPeso(String peso) {
		this.peso = peso;
	}
	public String getPuerto_usb() {
		return puerto_usb;
	}
	public void setPuerto_usb(String puerto_usb) {
		this.puerto_usb = puerto_usb;
	}
	public String getResolucion() {
		return resolucion;
	}
	public void setResolucion(String resolucion) {
		this.resolucion = resolucion;
	}
	
	
	
}
