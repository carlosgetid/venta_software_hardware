package com.empresa.proyecto.bean;

public class Catalogo {

	private String cod_catalogo;
	private String cod_subcatalogo;
	private String cod_tabla;
	private String descrip_catalogo;
	private String valor1;
	private int valor2;
	
	
	
	public String getCod_catalogo() {
		return cod_catalogo;
	}
	public void setCod_catalogo(String cod_catalogo) {
		this.cod_catalogo = cod_catalogo;
	}
	public String getCod_subcatalogo() {
		return cod_subcatalogo;
	}
	public void setCod_subcatalogo(String cod_subcatalogo) {
		this.cod_subcatalogo = cod_subcatalogo;
	}
	public String getCod_tabla() {
		return cod_tabla;
	}
	public void setCod_tabla(String cod_tabla) {
		this.cod_tabla = cod_tabla;
	}
	public String getDescrip_catalogo() {
		return descrip_catalogo;
	}
	public void setDescrip_catalogo(String descrip_catalogo) {
		this.descrip_catalogo = descrip_catalogo;
	}
	public String getValor1() {
		return valor1;
	}
	public void setValor1(String valor1) {
		this.valor1 = valor1;
	}
	public int getValor2() {
		return valor2;
	}
	public void setValor2(int valor2) {
		this.valor2 = valor2;
	}
	
	
	
	
	
}
