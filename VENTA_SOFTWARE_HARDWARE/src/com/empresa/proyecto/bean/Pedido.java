package com.empresa.proyecto.bean;

public class Pedido {

	private int cod_ped;

	private int id_usu;
	private String desc_usu;

	private int id_hor;
	private String fecha_hor;

	
	private int id_prod;
	private String nom_prod;
	
	
	
	
	public int getCod_ped() {
		return cod_ped;
	}
	public void setCod_ped(int cod_ped) {
		this.cod_ped = cod_ped;
	}
	public int getId_usu() {
		return id_usu;
	}
	public void setId_usu(int id_usu) {
		this.id_usu = id_usu;
	}
	public String getDesc_usu() {
		return desc_usu;
	}
	public void setDesc_usu(String desc_usu) {
		this.desc_usu = desc_usu;
	}
	public int getId_hor() {
		return id_hor;
	}
	public void setId_hor(int id_hor) {
		this.id_hor = id_hor;
	}
	public String getFecha_hor() {
		return fecha_hor;
	}
	public void setFecha_hor(String fecha_hor) {
		this.fecha_hor = fecha_hor;
	}
	public int getId_prod() {
		return id_prod;
	}
	public void setId_prod(int id_prod) {
		this.id_prod = id_prod;
	}
	public String getNom_prod() {
		return nom_prod;
	}
	public void setNom_prod(String nom_prod) {
		this.nom_prod = nom_prod;
	}
	
	
	
	
	
	
	
	
	
	
	
	
}
